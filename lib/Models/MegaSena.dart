
class MegaSena{
  int numeroUm;
  int numeroDois;
  int numeroTres;
  int numeroQuatro;
  int numeroCinco;
  int numeroSeis;

  MegaSena.Empty(){
    this.numeroUm = 0;
    this.numeroDois = 0;
    this.numeroTres = 0;
    this.numeroQuatro = 0;
    this.numeroCinco = 0;
    this.numeroSeis = 0;
  }
  MegaSena(this.numeroUm, this.numeroDois, this.numeroTres, this.numeroQuatro,
      this.numeroCinco, this.numeroSeis);



}